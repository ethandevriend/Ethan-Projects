package project.logic;

public class Card
{	
	
	/*
	 * Wild Card = -1
	 * Red = 0
	 * Yellow = 1
	 * Green = 2
	 * Blue = 3
	 */
	
	public int color = 0;
	
	/*
	 * 0 - 9 , self-explanatory.
	 * 10 - draw two
	 * 11 - change direction
	 * 12 - skip turn
	 * 13 - normal wild card
	 * 14 - draw four wild card
	 */
	public int number = 0;

}
